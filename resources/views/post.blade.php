                        <div class="title">Jon Rahm and Danny Willett widen their lead over the BMW PGA Championship field.</div>

                        <div class="title-box">
                            <p style="padding:10px; margin: 0;" class="mini">Spain's Jon Rahm celebrates on the 18th during day three of the BMW PGA Championship at Wentworth Golf Club, Surrey. (Photo by Bradley Collyer/PA Images via Getty Images)</p>
                        </div>
                        <img src="style/img/banner1.png" alt="" class="banner">

                        <p class="text">IRGINIA WATER, England -- It was a case of "as you were" at the top of the leader board after the third round of the BMW PGA Championship at Wentworth. The names at the summit -- Jon Rahm and Danny Willett -- remain the same after both shot four-under par 68s to reach 15-under par for the 54 holes played. There is one difference though. The gap between the leaders and the rest has increased. Where once it was two shots, it is now three, Justin Rose, Christian Bezuidenhout and Shubhankar Sharma the nearest pursuers. No one else is within five shots.</p>
                        <p class="text">For long enough, though, any advantage heading into the final round appeared to be going Rahm’s way. Two-shots clear with two holes to play after making six birdies against one bogey, the burly Spaniard finished 6-5 (bogey, par) that could easily have been 6-7 against Willett’s 5-4 to leave the pair tied again.</p>

                        <p class="text">“I’m happy I made par on 18,” said Rahm, who drove unplayable into a bush, had to hack-out for three, found the green from 200-yards out with his fourth shot, then holed from 20 feet. “It's a nice finish in that sense. Apart from the last two holes, the rest of the round was almost bulletproof. I played really good golf. I had a good plan going and if I had finished properly, I would have a lead.</p>

                        <p class="text">WATCH NOWGUESS THOSE CALVES! - PGA CHAMPIONSHIP EDITION</p>

                        <p class="text">“Still, there is a lot of golf to come. Danny is tied with me and playing solid. Plus the guys behind us are only three shots back. It’s going to be a tough day. I’m going to have to play really, really good golf to win this event. I hope I can live up to it.” </p>

                        <p class="text">Not surprisingly, Willett was in finer fettle at the end of a round that, like Rahm’s, was a mix of six birdies and two dropped shots. One of those bogeys came on the opening hole, but was quickly followed by five threes in six holes.</p>

                        <p class="text">RELATED: The Americans are finding the European Tour's flagship event to their liking</p>

                        <p class="text">“This is the biggest tournament of the year for the British guys who play the European Tour,” said the 2016 Masters champion, who hails from Yorkshire. “This is just a massive event. I've been here now 10 straight years and I love the place. I've won all around the world but it would be amazing to get it done for the home crowd. It's going to be a pretty tough battle tomorrow. Jon is up there and he's an incredible player. He’s only been on tour for three or four years and already he’s done some exceptional things.”</p>

                        <p class="text">A bit further down the board and much earlier, Rory McIlroy returned to something like his best form with a nifty seven-under par 65. Six birdies and an eagle littered the world No. 2's card, but on six-under par and T-18 he is surely too far back to contemplate a second victory in this event. Which is not to say he has completely given up hope.</p>

                        <p class="text">“I'll go out and give it a go,” he said with a shrug. “I'll try to shoot a similar score to what I did today and see where that leaves me. I can only do what I can. No matter if I'm five back or seven back or ten back, my mindset is the same. I've just got to go out and shoot a good score tomorrow. I was never thinking about winning this tournament after the first day, anyway. So I guess to put myself in a position where it's at least a faint hope is a good thing.”</p>

                        <p class="text">Amidst all of the above, a shot of the day wasn’t difficult to identify. From 220-yards out on the par-5 18th, Ross Fisher found the bottom of the cup to record only the third albatross in the history of this 64-year old event. For his "trouble," he won a BMW i8 Roaster worth $166,000. As they say in these parts, “a bit tasty mate.”</p>
